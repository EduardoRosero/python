def suma(num_uno, num_dos):
	"""Recibe dos numeros como parametros y devuelve la suma de los mismos"""
	return num_uno+num_dos

def resta(num_uno, num_dos):
	"""Recibe dos numeros como parametros y devuelve la diferencia de los mismos"""
	return num_uno-num_dos

def multiplicacion(num_uno, num_dos):
	"""Recibe dos numeros como parametros y devuelve la multiplicacion de los mismos"""
	return num_uno*num_dos

def division(num_uno, num_dos):
	"""Recibe dos numeros como parametros y devuelve la division de los mismos"""
	return num_uno/num_dos